// Aggregate results/*.jsonl into REPORT.md (host-side; run with `bun report.ts`).
// Re-runnable: it only replaces the block between the AUTO markers, so any
// hand-written findings around it are preserved.
import { readdirSync, readFileSync, writeFileSync, existsSync } from "node:fs";
import path from "node:path";

const HERE = import.meta.dir;
const RESULTS = path.join(HERE, "results");
const REPORT = path.join(HERE, "REPORT.md");
const START = "<!-- AUTO:START -->";
const END = "<!-- AUTO:END -->";

type Row = { scenario: string; step: string; status: string; wall_s: number; max_rss_kb: number; out_bytes: number };

const mb = (b: number) => (b / 1024 / 1024).toFixed(1);
const rssMb = (kb: number) => (kb / 1024).toFixed(0);

const rows: Row[] = [];
for (const f of readdirSync(RESULTS).filter((f) => f.endsWith(".jsonl")).sort()) {
  for (const line of readFileSync(path.join(RESULTS, f), "utf8").split("\n")) {
    if (line.trim()) rows.push(JSON.parse(line));
  }
}

const env = existsSync(path.join(RESULTS, "_env.txt"))
  ? readFileSync(path.join(RESULTS, "_env.txt"), "utf8").trim()
  : "(unknown)";

const scenarios = [...new Set(rows.map((r) => r.scenario))].sort();

let table = `| Scenario | Step | Wall (s) | Peak RSS (MB) | Output (MB) | Status |\n`;
table += `|---|---|--:|--:|--:|:--:|\n`;
for (const s of scenarios) {
  const steps = rows.filter((r) => r.scenario === s && r.step !== "_container_peak");
  const peak = rows.find((r) => r.scenario === s && r.step === "_container_peak");
  const totalWall = steps.reduce((a, r) => a + r.wall_s, 0);
  for (const r of steps) {
    table += `| ${s} | ${r.step} | ${r.wall_s.toFixed(1)} | ${rssMb(r.max_rss_kb)} | ${mb(r.out_bytes)} | ${r.status === "ok" ? "✅" : "❌"} |\n`;
  }
  if (steps.length > 1) table += `| **${s}** | **total** | **${totalWall.toFixed(1)}** | — | — | |\n`;
  if (peak) table += `| ${s} | _container peak_ | — | ${rssMb(peak.max_rss_kb)} | — | |\n`;
}

const auto = [
  START,
  "",
  "_Generated by `bun report.ts` from `results/*.jsonl`. Toolchain:_",
  "",
  "```",
  env,
  "```",
  "",
  table,
  "",
  "- **Peak RSS** = per-step `/usr/bin/time -f %M`; **container peak** = cgroup",
  "  `memory.peak` across the whole scenario (cross-check).",
  "- Wall-time is from Docker Desktop on macOS (a Linux VM) — indicative, not a",
  "  bare-metal server number. Peak RAM transfers well.",
  "",
  END,
].join("\n");

let body: string;
if (existsSync(REPORT)) {
  const cur = readFileSync(REPORT, "utf8");
  const i = cur.indexOf(START), j = cur.indexOf(END);
  body = i >= 0 && j > i ? cur.slice(0, i) + auto + cur.slice(j + END.length) : cur.trimEnd() + "\n\n" + auto + "\n";
} else {
  body = `# Performance report\n\nSee [README.md](README.md) for the method.\n\n## Findings\n\n_TODO: interpretation._\n\n## Measurements\n\n${auto}\n`;
}
writeFileSync(REPORT, body);
console.log(`Wrote ${REPORT} (${scenarios.length} scenarios, ${rows.length} rows).`);
